# n   m
# 4 * 3 = 3 + 3 + 3 + 3

#           nm
# 0 * 3 = 0
# 1 * 3 = (0 * 3) + 3 = 3
# 2 * 3 = (1 * 3) + 3 = 6
# 3 * 3 = (2 * 3) + 3 = 9
# 4 * 3 = (3 * 3) + 3 = 12

n = 4
m = 3
nm = 0 # nm - поточне значення n * m
k = 0 # фактично поточна кількість разів додавання
while k < n:
    k += 1
    nm += m

print(nm)


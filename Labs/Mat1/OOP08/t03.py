if __name__ == '__main__':
    N = int(input("N = "))
    u = 5   # попередній член послідовності
    w = 19  # поточний член послідовності
    for n in range(3, N+1):
        F = 5 * w - 6 * u
        u = w
        w = F

    print(f"Визначник порядку {N} = {w}")